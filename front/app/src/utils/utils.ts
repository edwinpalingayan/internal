/** ------------------------------------------------------------------------------------------------
 * 指定された値が配列に含まれているか検索する
 * @param   {Array}   array 検索対象の配列
 * @param   {*}       value 検索する値
 * @returns {boolean}       値が配列に含まれている場合はtrue、それ以外はfalse
 */ //------------------------------------------------------------------------------------------------
export interface IsInArrayFn<T> {
  (value: T, array: T[]): boolean;
}

export const isInArray: IsInArrayFn<unknown> = (value, array) =>
  array.includes(value);
